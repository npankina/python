    swim_or_wait = input("What do you choose? Swim the river - enter S or Wait - W: ").lower()
    if swim_or_wait == 's':
        print("Attacked by trout. \n Game over.")
    else:
        door = input("Which door do you prefer? Red (R), Yellow (Y), Blue (B) or anything else? ").lower()
